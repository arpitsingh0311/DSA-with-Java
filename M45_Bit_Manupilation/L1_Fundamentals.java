// package M45_Bit_Manupilation;

public class L1_Fundamentals {
    public static void main(String[] args) {
        System.out.println(5|9);
        System.out.println(5&9);
        // 1's complement
        System.out.println(~6);
        // 2's complement
        System.out.println(~6+1);
    }
}
